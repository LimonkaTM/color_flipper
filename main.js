const btnsModeArray = document.querySelectorAll('.header__btn-random-mode');
const btnChangeColor = document.querySelector('.main__btn-flip-color');

const textColor = document.querySelector('.main__color');

updateAllElements( getRandomColorName() )

btnsModeArray.forEach( elem => {
  elem.addEventListener('click', () => {
    removeAllActiveBtns(btnsModeArray)

    elem.classList.add('header__btn-random-mode--active')
  })
})

function removeAllActiveBtns(btnsArr) {
  btnsArr.forEach( elem => elem.classList.remove('header__btn-random-mode--active') )
}

function updateAllElements(value){
  const colorBlock = document.querySelector('.main__color-block');
  // const main = document.querySelector('.main');

  textColor.textContent = value;
  colorBlock.style.backgroundColor = value;
  // main.style.backgroundColor = value;
}

function getRandomNumber(max) {
  return Math.floor( Math.random() * max )
}

function getRandomHex() {
  let hex = '#';

  for (let i = 0; i < 6; i++) {
    hex += HEX_ALPHABET[ getRandomNumber(HEX_ALPHABET.length) ];
  }

  return hex
}

function getRandomColorName() {
  return CSS_COLOR_NAMES[ getRandomNumber(CSS_COLOR_NAMES.length) ]
}

btnChangeColor.addEventListener('click', () => {
  if ( btnsModeArray[1].classList.contains('header__btn-random-mode--active') ) {
    updateAllElements( getRandomHex() ) 
    
    return
  }

  if ( btnsModeArray[0].classList.contains('header__btn-random-mode--active') ) {
    updateAllElements( getRandomColorName() )
    
    return
  }
})

textColor.addEventListener('click', () => navigator.clipboard.writeText(textColor.textContent) )